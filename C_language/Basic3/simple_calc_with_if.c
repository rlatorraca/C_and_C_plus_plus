/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    float number01 = 0, number02 = 0;
    int operator;

    printf("Enter FIRST number: ");
    scanf("%f", &number01);

    printf("Enter SECOND number: ");
    scanf("%f", &number02);

    printf("[1] - Addition\n");
    printf("[2] - Subtracion\n");
    printf("[3] - Implementation\n");
    printf("[4] - Division\n");
    printf("Enter you option:\n");
    scanf("%d", &operator);

    if (operator== 1)
    {
        printf("\nResult: %.2f + %.2f = %.2f\n\n", number01, number02, number01 + number02);
    }
    else if (operator== 2)
    {
        printf("\nResult: %.2f - %.2f = %.2f\n\n", number01, number02, number01 - number02);
    }
    else if (operator== 3)
    {
        printf("\nResult: %.2f x %.2f = %.2f\n\n", number01, number02, number01 * number02);
    }
    else if (operator== 4)
    {
        printf("\nResult: %.2f / %.2f = %.2f\n\n", number01, number02, number01 / number02);
    }
    else
    {
        printf("\nInvalid Option\n");
    }

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

