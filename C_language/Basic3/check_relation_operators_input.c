/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    /*
        Operadores Relacionais em C:
            a) == (igualdade)
            b) > (maior que)
            c) >= (maior ou igual que)
            d) < (menor que)
            e) <= (menor ou igual que)
            f) != (diferente de)
    */

    float a = 0.0, b = 0.0;

    printf("Enter a value for A: ");
    scanf("%f", &a);

    printf("Enter a value for B: ");
    scanf("%f", &b);

    printf("Q: %.2f == %.2f; A: %d      [0 => False / Other => True]\n", a, b, a == b);
    printf("Q: %.2f > %.2f; A: %d       [0 => False / Other => True]\n", a, b, a > b);
    printf("Q: %.2f >= %.2f; A: %d      [0 => False / Other => True]\n", a, b, a >= b);
    printf("Q: %.2f < %.2f; A: %d       [0 => False / Other => True]\n", a, b, a < b);
    printf("Q: %.2f <= %.2f; A: %d      [0 => False / Other => True]\n", a, b, a <= b);
    printf("Q: %.2f != %.2f; A: %d      [0 => False / Other => True]\n", a, b, a != b);

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

