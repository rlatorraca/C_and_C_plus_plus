    if (option == 'y' || option == 'Y')
    {
        printf("Your choice is YES.\n");
    }
    else if (option == 'n' || option == 'N')
    {
        printf("Your choice is NO.\n");
    }
    else
    {
        printf("Your choice is INCORRECT.\n");
    }

    /* Press Enter to continue ... */
