/* ========================================================================= */
/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    float distance_cm = 0.0, temporary = 0.0;

    printf("Enter distance in centimeters(cm): ");
    scanf("%f", &distance_cm);

    if (distance_cm <= 25.0)
    {
        printf("ROBOT is change his/her direction. \n");
        temporary = distance_cm;
    }
    else
    {
        printf("No Changes on direction. \n");
    }

    if (temporary != 0.0)
        printf("Robot detected an obstacle in %.2f cm. \n", temporary);
    else
        printf("No obstacle detected. \n");

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

