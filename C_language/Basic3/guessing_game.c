/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    int value, guess;

    printf("Enter a value between 0 to 10 : ");
    scanf("%d", &value);

    if (value < 0 || value > 10)
    {
        printf("Invalid value. Number is \x82 5.\a\n");
        value = 5;
        system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/
    }
    system("clear"); /* Unix / Linux = Clear screen */

    printf("Guess a number:");
    scanf("%d", &guess);

    if (guess == value)
    {
        printf("Your Guess \x82 %d is CORRECT\n", value);
    }
    else if (guess > value)
    {
        printf("ERROR - Your Guess \x82 %d is LOWER\n", value);
    }
    else
    {
        printf("ERROR - Your Guess \x82 %d is HIGHER\n", value);
    }

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

