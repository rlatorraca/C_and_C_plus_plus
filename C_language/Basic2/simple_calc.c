/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    float x = 0.0, y = 0.0, result = 0.0;

    printf("Enter number 01: ");
    scanf("%f", &x);

    printf("Enter number 02: ");
    scanf("%f", &y);

    printf("\nResults:\n");
    printf("========================================\n");

    result = x + y;
    printf("Sum             [%.2f + %.2f]: %.2f\n", x, y, result);

    result = x - y;
    printf("Subtration      [%.2f - %.2f]: %.2f\n", x, y, result);

    result = x * y;
    printf("Multiplication  [%.2f x %.2f]: %.2f\n", x, y, result);

    result = x / y;
    printf("Division        [%.2f / %.2f]: %.2f\n", x, y, result);

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

