/* ============================================================================

    Program: Cacula a frequencua de corte de um filtro passa-baixas

        Equacao : f = 1(2π * R * C )

        - capacitor : 1µF ( = 1.0e-6)
        - resistor : 3k Ohm

    Resultado com frequencia em 2 casas de precisao

    Author: RLSP
    Created: September 2022
    Modified: September 2022

============================================================================ */

/* ========================================================================= */
/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    double pi = 3.14159265358979,
           frequency = 0.0,
           C = 1.0e-6, /* capacitor */
        R = 3.0e3;     /* resistor */

    frequency = 1 / (2 * pi * R * C);

    printf("[Cientific] Frequency = 1 / (2 * %.12e * %.2e * %.2e) = %.2e Hertz\n\n", pi, R, C, frequency);
    printf("[Float]     Frequency = 1 / (2 * %.12f * %.2f * %.6f) = %.2f Hertz\n\n", pi, R, C, frequency);

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

