/* ========================================================================= */
/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    float tempCelsius = 0.0, tempFahrenheit = 0.0;

    printf("Enter the temperatures in Celsius: ");
    scanf("%f", &tempCelsius);

    tempFahrenheit = (1.8 * tempCelsius) + 32;

    printf("%.2f Celsius is %.2f Fahrenheit\n", tempCelsius, tempFahrenheit);

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

