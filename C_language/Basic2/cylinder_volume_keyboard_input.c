/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    float pi = 3.14159265358979, r = 0.0, h = 0.0, volume = 0.0;

    printf("Enter the radius of the cylinder in meters : ");
    scanf("%f", &r);

    printf("Enter the height of the cylinder in meters : ");
    scanf("%f", &h);

    volume = pi * r * r * h;

    printf("Cylinder volume in cubic meters is: %.4f\n", volume);

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

