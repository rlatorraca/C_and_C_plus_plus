/* --- MAIN FUNCTION --- */
int main()
{
    float x = 0.01, y = 0.02, result = 0.03;

    printf("Enter number 01: ");
    scanf("%f", &x);

    printf("Enter number 02: ");
    scanf("%f", &y);

    result = x + y;
    printf("Sum of %.2f + %.2f = %.2f\n", x, y, result);

    /* Lendo na mesma linha (deve ser separados por SPACE or ENTER*/
    printf("Enter number 01 e 02: ");
    scanf("%f%f", &x, &y);

    result = x + y;
    printf("Sum of %.2f + %.2f = %.2f\n", x, y, result);

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

