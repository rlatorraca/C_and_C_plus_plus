/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>
#include <locale.h>

/* --- FUNCTIONS PROTOTYPE --- */
void print_special_characters(char figure, int quantity);

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    setlocale(LC_ALL, "fr_Canada");

    unsigned number;

    while (1)
    {
        printf("Enter a number: ");
        scanf("%d", &number);
        if (number == 1)
            print_special_characters('#', 1);
        if (number == 5)
            print_special_characters('*', 5);
        if (number == 10)
            print_special_characters('$', 10);

        if (!number) /* Para verificar se eh ZERO = FALSE*/
            break;

    } /* END while*/

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

/* ========================================================================= */
/* --- FUNCTIONS --- */

void print_special_characters(char figure, int quantity)
{
    for (unsigned i = 0; i < quantity; i++)
        printf("%c", figure);
    printf("\n");

} /* END  print_special_characters function */

