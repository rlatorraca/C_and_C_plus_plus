/* --- FUNCTIONS PROTOTYPE --- */
double exponential(float base, int exponent);

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    setlocale(LC_ALL, "fr_Canada");

    float base;
    unsigned exponent;

    while (1)
    {
        printf("Enter a number BASE: ");
        scanf("%f", &base);

        printf("Enter a number EXPONENT: ");
        scanf("%d", &exponent);

        if (base == 99 && exponent == 99) /* Para verificar se eh ZERO = FALSE*/
            break;

        printf("%.2f ^ %d = %.2f\n\n", base, exponent, exponential(base, exponent));

    } /* END while*/

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

/* ========================================================================= */
/* --- FUNCTIONS --- */

double exponential(float base, int exponent)
{
    float result = 1.0;
    for (short i = 0; i < exponent; i++)
    {
        result *= base;
    }

    return result;

} /* END  print_div_and_rest function */

