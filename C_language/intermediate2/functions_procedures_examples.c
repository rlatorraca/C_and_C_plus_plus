/* ========================================================================= */
/* --- FUNCTIONS --- */

void frames(int number_lines, int number_columns)
{

    int i, j;

    for (i = 0; i < number_lines; i++)
    {
        for (j = 0; j < number_columns; j++)
        {
            putchar('#');
        }
        printf("\n");
    }
} /* END  frames function */

