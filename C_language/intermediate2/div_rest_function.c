/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    setlocale(LC_ALL, "fr_Canada");

    unsigned number01, number02;

    while (1)
    {
        printf("Enter a number 01: ");
        scanf("%d", &number01);

        printf("Enter a number 01: ");
        scanf("%d", &number02);

        if (!number01 && !number02) /* Para verificar se eh ZERO = FALSE*/
            break;

        print_div_and_rest(number01, number02);

    } /* END while*/

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

/* ========================================================================= */
/* --- FUNCTIONS --- */

void print_div_and_rest(int number01, int number02)
{
    printf("\nDivision of %d / %d = %d", number01, number02, number01 / number02);
    printf("\nRest of %d %% %d = %d\n\n", number01, number02, number01 % number02);

} /* END  print_div_and_rest function */

