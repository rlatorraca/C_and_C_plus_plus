/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>
#include <locale.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    setlocale(LC_ALL, "fr_Canada");

    printf("\n");

    for (char i = 0; i < 10; i++)
    {
        printf("%d ", i);
    }
    printf("\n\n");

    /* Imprime o cubo para cada numero do loop, de forma descrecente*/
    for (char i = 10; i >= 1; i--)
    {
        printf("Cube of %2d [%2d x %2d x %2d ] = %4d\n", i, i, i, i, i * i * i);
    }

    printf("\n");
    /* Press Enter to exit ... */
    system("read -p '\nPress Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

