#include <stdlib.h>
#include <locale.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    setlocale(LC_ALL, "fr_Canada");

    int number;

    while (1)
    {
        printf("Enter a number: ");
        scanf("%d", &number);

        if (number % 2) /* se for 1, sera TRUE, 1 = impar / 0 = par, pois tera resto 1*/
        {
            break;
        }

        printf("All good !!!\n\n");
    }

    printf("\nOdd number[%d] entered !!!\n ", number);

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

