/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    int table = 0, val = 0;

    printf("Times table for : ");
    scanf("%d", &table);

    while (val <= 10)
    {
        printf("%d x %2d = %3d\n", table, val, table * val); /* %3d => reserva 3 espacos dna tela para impressao */
        val++;
    }
    val = 10;
    printf("\n\n");

    while (val) /* teste ate que val = FALSE, ou seja  = 0*/
    {
        printf("%d x %2d = %3d\n", table, val, table * val); /* %3d => reserva 3 espacos dna tela para impressao */
        val--;
    }

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

