/* ========================================================================= */
/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{
    int x = 5, y = 8;
    int x_final = 0, y_final = 0;

    printf("x initial = %d\n", x);
    printf("y inital = %d\n\n", y);

    x_final = ++x; // incrementa PRIMEIRO
    y_final = ++y;

    printf("x incremented pre= %d\n", x_final);
    printf("y incremented pre = %d\n\n", y_final);

    x_final = --x; // decrementa PRIMEIRO
    y_final = --y;

    printf("x decremented pre = %d\n", x_final);
    printf("y decremented pre = %d\n\n", y_final);

    x_final = x++; // Atribui PRIMEIRO, e depois incrementa
    y_final = y++;

    printf("x incremented pos= %d\n", x_final);
    printf("y incremented pos = %d\n\n", y_final);

    x_final = x--; // Atribui PRIMEIRO, e depois decrementa
    y_final = y--;

    printf("x decremented pos = %d\n", x_final);
    printf("y decremented pos = %d\n\n", y_final);

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

