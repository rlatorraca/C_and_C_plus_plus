/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    double counter = 0;

    while (1) /* 1 = TRUE => ocorrera um Loop infinito */
    {
        printf("RLSP[%.1f]\n", counter); /* %3d => reserva 3 espacos dna tela para impressao */
        counter++;
    }

    /* Press Enter to exit ... */
    system("read -p 'Press Enter to exit...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

