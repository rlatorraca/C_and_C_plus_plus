/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>
#include <locale.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    setlocale(LC_ALL, "fr_Canada");

    int i, j, steps;
    char figure = 35;

    printf("How many steps do you want on your stair: ");
    scanf("%d", &steps);

    for (i = 0; i < steps; i++)
    {
        for (j = steps; j >= steps - i; j--)
        {
            printf("%c", figure);
        }
        printf("\n");
    }

    printf("\n");

    /* Press Enter to exit ... */
    system("read -p '\nPress Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

