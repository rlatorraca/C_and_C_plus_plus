/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>
#include <math.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main()
{

    float a = 23.34;
    float b = 12.45;
    float c = 32.56;
    int d = 44;

    a = a / 2.8;
    printf("a = a + %.2f\n", a);

    a = 23.34;
    a /= 2.8;
    printf("a /= 2.8 = %.2f\n\n", a);

    b = b * (0.8 + 1.3);
    printf("b = b * (0.8 + 1.3) = %.2f\n", b);

    b = 12.45;
    b *= 0.8 + 1.3;
    printf("b *= 0.8 + 1.3 = %.2f\n\n", b);

    c = c / (c + 2.5) + 33;
    printf("c = c / (c + 2.5) + 33 =  %.2f\n", c);

    c = 32.56;
    c /= (c + 2.5) + 33;
    printf("c /=  (c + 2.5) + 33 = %.2f\n\n", c);

    d = (d % 4) + d;
    printf("d = d %% 2 + d = %.2d\n", d);

    d = 44.32;
    d %= 4 + d;
    printf("d %%= 4 + d = %.2d\n\n", d);

    printf("\n");

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

