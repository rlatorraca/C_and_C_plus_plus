/* --- LIBRARIES --- */
#include <stdio.h>
#include <stdlib.h>
#include <locale.h>
#include <string.h>

/* ========================================================================= */
/* --- MAIN FUNCTION --- */
int main() /* int de returna em Funcoes em C Ã© OPTIONAL*/
{
    // Set all categories and return "en-US"
    setlocale(LC_ALL, "en-US");

    int *p1 = NULL, *p2 = NULL;
    int a1, a2;

    a1 = 1;
    a2 = 3;

    p1 = &a1;
    p2 = &a2;

    printf("a1 address [&a1]: %p\n", &a1);
    printf("a2 address [&a2]: %p\n\n", &a2);

    printf("Value a1 [&a1]: %d\n", &a1);
    printf("Value a2 [&a2]: %d\n\n", &a2);

    printf("Value a1 [a1]: %d\n", a1);
    printf("Value a2 [a2]: %d\n\n", a2);

    printf("p1 point to address of &a1 [p1]: %p\n", p1);
    printf("p2 point to address of &a2 [p2]: %p\n\n", p2);

    printf("p1 address [&p1]: %p\n", &p1);
    printf("p2 address [&p2]: %p\n\n", &p2);

    printf("Value p1 [&p1]: %p\n", &p1);
    printf("Value p2 [&p2]: %p\n\n", &p2);

    printf("Value p1 [*p1]: %d\n", *p1);
    printf("Value p2 [*p2]: %d\n\n", *p2);

    (p1 > p2) ? printf("P1 address is higher than P2\n\n") : printf("P2 address is higher than P1\n\n");

    /* Press Enter to continue ... */
    system("read -p 'Press Enter to continue...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is exectued correctly  */

} /* END  main function */

