int main() /* int de returna em Funcoes em C Ã© OPTIONAL*/
{
    setlocale(LC_ALL, "fr_Canada");

    FILE *file_rlsp, *file_rlsp_encrypt;
    int char_read_file;
    int size = 0, letter_a_counter = 0, number_lines_counter = 0;

    // Ler um arquivo para fazer a criptografia
    if ((file_rlsp = fopen("file_to_crypt.txt", "r")) == NULL)
    {
        printf("ERROR - writing ...\n");
        system("pause");
        exit(1);
    }

    // Cria um arquivo criptgrafado
    if ((file_rlsp_encrypt = fopen("file_crypted.txt", "w")) == NULL)
    {
        printf("ERROR - writing ...\n");
        system("pause");
        exit(1);
    }

    while ((char_read_file = fgetc(file_rlsp)) != EOF)
    {
        printf("%c", char_read_file);
        fputc(char_read_file + 3, file_rlsp_encrypt);
    }

    fclose(file_rlsp);
    fclose(file_rlsp_encrypt);

    /* Press Enter to exit ... */
    system("read -p '\nPress Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

