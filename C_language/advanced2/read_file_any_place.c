int main() /* int de returna em Funcoes em C Ã© OPTIONAL*/
{
    setlocale(LC_ALL, "fr_Canada");

    FILE *file_rlsp;
    int char_read_file;
    char file_name[300] = "";

    printf("Enter a file name and extension [ file_name.extesion]: ");
    fgets(file_name, sizeof(file_name), stdin);
    file_name[strcspn(file_name, "\n")] = 0; // Retira p ENTER do fgets

    file_rlsp = fopen(file_name, "r");

    // Abre ao arquivo para leitura
    if (file_rlsp == NULL)
    {
        printf("ERROR - reading ...\n");
        system("read -p '\nPress Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/
        exit(1);
    }

    while ((char_read_file = fgetc(file_rlsp)) != EOF)
    {
        printf("%c", char_read_file);
    }

    fclose(file_rlsp);

    /* Press Enter to exit ... */
    system("read -p '\nPress Enter to exit...[RLSP]\n' key"); /* PAUSE Exceution - LINUX*/

    /*  system("PAUSE"); PAUSE Exceution - WINDOWS*/
    return 0; /* return 0, if MAIN function is executed correctly  */

} /* END  main function */

